def load_norm():
    d = {}
    with open(config.NORMALIZED_TYPED, encoding="utf-8") as f:
        next(f)
        for line in f:
            rsid, chrom, pos, a1, a2, zyg, amb = line.rstrip("\n").split("\t")
            d[rsid] = (a1, a2)
    return d


def run():
    norm = load_norm()
    with open(config.OUT / "09_traits.tsv", "w", encoding="utf-8") as out:
        out.write("gene\ttrait\tgenotype\teffect_copies\tinterpretation\n")
        for rsid, (gene, trait, eff, interp) in TRAITS.items():
            gt = norm.get(rsid)
            if gt is None:
                out.write(f"{gene}\t{trait}\t—\t—\tнет на чипе V5\n")
                continue
            a1, a2 = gt
            copies = [a1, a2].count(eff)
            out.write(f"{gene}\t{trait}\t{a1}{a2}\t{copies}\t{interp[copies]}\n")
    print(f"[step9] трейты: {len(TRAITS)} маркеров → out/09_traits.tsv")


if __name__ == "__main__":
    run()
